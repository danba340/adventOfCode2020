const tiles = testInput.split("\n\n").map(tile => {
    const [header, ...rows] = tile.split("\n")
    const id = header.split(" ")[1]
    return {
        id: id.substring(0, id.length - 1),
        rows
    }
})

const getLeftSide = (tile) => {
    return tile.rows.map(row => {
        return row[0]
    }).join("")
}
const getRightSide = (tile) => {
    return tile.rows.map(row => {
        return row[9]
    }).join("")
}

const tilesWithMatches = tiles.map((tile, index) => {
    const matches = []
    for(const [matchIndex,matchTile] of tiles.entries()){
        if(index === matchIndex){
            continue;
        }
        if(tile[0] === matchTile[0]){
            matches.push({
                ...tile,
                matchId: matchTile.id,
                description: "top-top"
            })
        }
    }
})
